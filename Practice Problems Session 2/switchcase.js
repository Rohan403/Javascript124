//Write a program using switch case to check if a character is a vowel or a consonent.
char = prompt("Enter a character");
switch(char){
    case 'a':
    case 'A':
        alert("It is a vowel");
        break;
    case 'e':
    case 'E':
        alert("It is a vowel");
        break;
    case 'i':
    case 'I':
        alert("It is a vowel");
        break;
    case 'o':
    case 'O':
        alert("It is a vowel");
        break;
    case 'u':
    case 'U':
        alert("It is a vowel");
        break;
    default:
        alert("It is a consonent");
}